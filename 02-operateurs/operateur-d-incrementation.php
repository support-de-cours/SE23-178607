<?php 

// Incrementation
// --
// notation : ++ 

$a = 10;

// Post incrementation $a++
// --
// -> on sort la valeur de $a
// -> puis on incrémente la valeur de $a
echo $a++ ."<br>";

// Pré incrementation ++$a
// --
// -> on incrémente la valeur de $a
// -> Puis on sort la valeur de $a
echo ++$a ."<br>";


echo "<hr>";

// Décrementation
// --
// notation : --

$b = 10;

// Post Décrementation $b--
// --
// -> on sort la valeur de $b
// -> puis on décrémente la valeur de $b
echo $b-- ."<br>";

// Pré Décrementation --$b
// --
// -> on décrémente la valeur de $b
// -> Puis on sort la valeur de $b
echo --$b ."<br>";
